void UART3_send_string(unsigned char *str)
{
	while(*str)
	{
		if(*str=='\r')UART3_send_char(0x0d);
			else if(*str=='\n')UART3_send_char(0x0a);
				else UART3_send_char(*str);
		str++;
	}
}

